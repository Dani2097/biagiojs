test('font subset: scan include testo da pages/*.page.biagio', () => {
  const dir = mkdtempSync(join(tmpdir(), 'biagio-subset-'));
  const pages = join(dir, 'pages');
  mkdirSync(pages, { recursive: true });
  writeFileSync(join(pages, 'index.page.biagio'), '<page title="t" /><component id="x"><template>Žlutý biagiojs</template></component>');
  const text = collectSubsetText(dir, [], { preset: null, scan: true, extra: '' });
  assert.match(text, /Žlutý biagiojs/);
});

