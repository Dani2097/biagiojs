test('src/: nessun prefisso errore [cvw] residuo', () => {
  const stale = [];
  for (const f of walkJs(join(root, 'src'))) {
    const src = readFileSync(f, 'utf8');
    if (/\[cvw[\]:]/.test(src)) stale.push(f.replace(root + '\\', '').replace(root + '/', ''));
  }
  assert.deepEqual(stale, [], `file con [cvw] residuo: ${stale.join(', ')}`);
});

