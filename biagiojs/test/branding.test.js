test('loadConfig legge biagio.config.js dal progetto', async () => {
  const dir = mkdtempSync(join(tmpdir(), 'biagio-cfg-'));
  writeFileSync(join(dir, 'biagio.config.js'), `export default { site: { name: 'Test', baseUrl: 'https://t.dev' } };`);
  const cfg = await loadConfig(dir);
  assert.equal(cfg.site.name, 'Test');
});

